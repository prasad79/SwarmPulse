	public static double[] generateRandomGPSCoords() {

		double minLat = -90.00, maxLat = 90.00, minLon = -180.00, maxLon = 180.00;
		double latitude = minLat
				+ (double) (Math.random() * ((maxLat - minLat) + 1));
		double longitude = minLon
				+ (double) (Math.random() * ((maxLon - minLon) + 1));
		DecimalFormat df = new DecimalFormat("#.#######");

		double gps[] = { latitude, longitude };
		return gps;
	}
	
	
	

